/// Packet action returned by TLS Proxy.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PacketAction {
    Unknown = 0,
    ForwardUnchanged = 1,
    ForwardModified = 2,
    Drop = 3,
    Reject = 4,
}

/// Direction of packet flow.
#[derive(Debug, Clone, Copy)]
pub enum PacketDirection {
    Inbound,
    Outbound,
}

/// Request sent to TLS Proxy for packet inspection.
#[derive(Debug, Clone)]
pub struct InterceptPacketRequest {
    pub connection_id: String,
    pub source_ip: String,
    pub source_port: i32,
    pub destination_ip: String,
    pub destination_port: i32,
    pub protocol: String,
    pub direction: PacketDirection,
    pub packet_data: Vec<u8>,
    pub interface_name: String,
}

/// Response from TLS Proxy after packet inspection.
#[derive(Debug, Clone)]
pub struct InterceptPacketResponse {
    pub action: PacketAction,
    pub packet_data: Vec<u8>,
    pub success: bool,
    pub error_message: String,
    pub processing_time_ms: i64,
    pub sni_hostname: String,
    pub resolved_ip: String,
}

/// Raw packet structure from packet capture.
#[derive(Debug, Clone)]
pub struct RawPacket {
    pub source_ip: String,
    pub source_port: u16,
    pub dest_ip: String,
    pub dest_port: u16,
    pub protocol: String,
    pub outbound: bool,
    pub payload: Vec<u8>,
    pub interface_name: String,
    pub timestamp: Instant,
}

