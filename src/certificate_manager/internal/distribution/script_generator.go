// GenerateLinuxScript generates a Bash script for Linux CA installation.
func GenerateLinuxScript(config *ScriptConfig) (string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return "", fmt.Errorf("GenerateLinuxScript: %w", err)
	}
	return renderTemplate(linuxScriptTemplate, config)
}

// GenerateWindowsScript generates a PowerShell script for Windows CA installation.
func GenerateWindowsScript(config *ScriptConfig) (string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return "", fmt.Errorf("GenerateWindowsScript: %w", err)
	}
	return renderTemplate(windowsScriptTemplate, config)
}

// GenerateMacScript generates a shell script for macOS CA installation.
func GenerateMacScript(config *ScriptConfig) (string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return "", fmt.Errorf("GenerateMacScript: %w", err)
	}
	return renderTemplate(macScriptTemplate, config)
}

// GenerateChromeOSScript generates instructions for ChromeOS CA installation.
func GenerateChromeOSScript(config *ScriptConfig) (string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return "", fmt.Errorf("GenerateChromeOSScript: %w", err)
	}
	return renderTemplate(chromeosInstructionsTemplate, config)
}

// GenerateFirefoxScript generates a script for Firefox CA installation.
func GenerateFirefoxScript(config *ScriptConfig) (string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return "", fmt.Errorf("GenerateFirefoxScript: %w", err)
	}
	return renderTemplate(firefoxScriptTemplate, config)
}

// ============================================================================
// Bulk Generation
// ============================================================================

// GenerateAllScripts generates all platform scripts in a single call.
// Returns a map of platform name to script content.
func GenerateAllScripts(config *ScriptConfig) (map[string]string, error) {
	if err := ValidateScriptConfig(config); err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: %w", err)
	}

	scripts := make(map[string]string)

	// Generate Linux script
	linux, err := GenerateLinuxScript(config)
	if err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: linux: %w", err)
	}
	scripts["linux"] = linux

	// Generate Windows script
	windows, err := GenerateWindowsScript(config)
	if err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: windows: %w", err)
	}
	scripts["windows"] = windows

	// Generate macOS script
	macos, err := GenerateMacScript(config)
	if err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: macos: %w", err)
	}
	scripts["macos"] = macos

	// Generate ChromeOS instructions
	chromeos, err := GenerateChromeOSScript(config)
	if err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: chromeos: %w", err)
	}
	scripts["chromeos"] = chromeos

	// Generate Firefox script
	firefox, err := GenerateFirefoxScript(config)
	if err != nil {
		return nil, fmt.Errorf("GenerateAllScripts: firefox: %w", err)
	}
	scripts["firefox"] = firefox

	return scripts, nil
}

// ============================================================================
// Validation
// ============================================================================

// ValidateScriptConfig validates the script configuration.
func ValidateScriptConfig(config *ScriptConfig) error {
	if config == nil {
		return errors.New("config cannot be nil")
	}

	if strings.TrimSpace(config.BaseURL) == "" {
		return ErrEmptyBaseURL
	}

	if strings.TrimSpace(config.CACertURL) == "" {
		return ErrEmptyCertURL
	}

	if strings.TrimSpace(config.CertificateName) == "" {
		return ErrEmptyCertName
	}

	// Validate URL formats
	if _, err := url.Parse(config.BaseURL); err != nil {
		return fmt.Errorf("%w: BaseURL: %v", ErrInvalidURL, err)
	}

	if _, err := url.Parse(config.CACertURL); err != nil {
		return fmt.Errorf("%w: CACertURL: %v", ErrInvalidURL, err)
	}

	return nil
}

// ============================================================================
// Template Rendering Helper
// ============================================================================

// renderTemplate executes a Go template with the given config.
func renderTemplate(templateStr string, config *ScriptConfig) (string, error) {
	tmpl, err := template.New("script").Parse(templateStr)
	if err != nil {
		return "", fmt.Errorf("%w: parse error: %v", ErrTemplateExecution, err)
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, config); err != nil {
		return "", fmt.Errorf("%w: execute error: %v", ErrTemplateExecution, err)
	}

	return buf.String(), nil
}

