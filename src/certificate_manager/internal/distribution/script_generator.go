// ============================================================================
// Script Configuration
// ============================================================================

// ScriptConfig contains parameters for script template rendering.
type ScriptConfig struct {
	BaseURL         string // HTTP server base URL (e.g., "http://192.168.1.1")
	CACertURL       string // Full URL to CA certificate (e.g., "http://192.168.1.1/ca.crt")
	CADerURL        string // Full URL to DER format certificate
	CertificateName string // CA certificate display name (e.g., "SafeOps Root CA")
	Organization    string // Organization name for display
	RequiresSudo    bool   // Whether script needs root privileges
	Fingerprint     string // SHA-256 fingerprint for verification (optional)
}

// ============================================================================
// Error Types
// ============================================================================

var (
	ErrEmptyBaseURL      = errors.New("base URL cannot be empty")
	ErrEmptyCertURL      = errors.New("CA certificate URL cannot be empty")
	ErrEmptyCertName     = errors.New("certificate name cannot be empty")
	ErrInvalidURL        = errors.New("invalid URL format")
	ErrTemplateExecution = errors.New("template execution failed")
)

// ============================================================================
// Linux Script Template
// ============================================================================

const linuxScriptTemplate = `#!/bin/bash
# SafeOps Root CA Installation Script for Linux
# Generated by SafeOps Certificate Manager
# Organization: {{.Organization}}
#
# Usage: sudo ./install-ca.sh

set -e  # Exit on error

CA_URL="{{.CACertURL}}"
CA_NAME="{{.CertificateName}}"
CERT_DIR="/usr/local/share/ca-certificates"
CERT_FILE="$CERT_DIR/safeops-root-ca.crt"

echo "=================================================="
echo " SafeOps Root CA Installation Script"
echo "=================================================="
echo ""

# Check if running as root
if [ "$EUID" -ne 0 ]; then
    echo "ERROR: Please run as root (sudo ./install-ca.sh)"
    exit 1
fi

# Create certificate directory if needed
mkdir -p "$CERT_DIR"

# Download CA certificate
echo "[1/3] Downloading CA certificate..."
if command -v curl >/dev/null 2>&1; then
    curl -fsSL -o "$CERT_FILE" "$CA_URL"
elif command -v wget >/dev/null 2>&1; then
    wget -q -O "$CERT_FILE" "$CA_URL"
else
    echo "ERROR: Neither curl nor wget found. Please install one."
    exit 1
fi

if [ ! -f "$CERT_FILE" ]; then
    echo "ERROR: Failed to download certificate"
    exit 1
fi

# Set correct permissions
chmod 644 "$CERT_FILE"

# Update CA trust store
echo "[2/3] Updating system CA trust store..."
if command -v update-ca-certificates >/dev/null 2>&1; then
    # Debian/Ubuntu
    update-ca-certificates
elif command -v update-ca-trust >/dev/null 2>&1; then
    # RHEL/CentOS/Fedora
    cp "$CERT_FILE" /etc/pki/ca-trust/source/anchors/
    update-ca-trust
else
    echo "ERROR: Could not find update-ca-certificates or update-ca-trust"
    echo "Please manually add $CERT_FILE to your system trust store"
    exit 1
fi

# Verification
echo "[3/3] Verifying installation..."
if [ -f "$CERT_FILE" ]; then
    echo ""
    echo "=================================================="
    echo " Installation Complete!"
    echo "=================================================="
    echo "✓ Certificate: $CA_NAME"
    echo "✓ Location: $CERT_FILE"
    echo "✓ Status: Trusted"
    {{if .Fingerprint}}echo "✓ Fingerprint: {{.Fingerprint}}"{{end}}
    echo ""
    echo "You may need to restart your browser for changes to take effect."
else
    echo "ERROR: Certificate verification failed"
    exit 1
fi
`

