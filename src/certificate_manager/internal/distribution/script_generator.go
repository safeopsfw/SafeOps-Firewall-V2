const windowsScriptTemplate = `# SafeOps Root CA Installation Script for Windows
# Generated by SafeOps Certificate Manager
# Organization: {{.Organization}}
#
# Usage: Right-click -> Run as Administrator

$ErrorActionPreference = "Stop"

$caUrl = "{{.CACertURL}}"
$caName = "{{.CertificateName}}"
$tempFile = "$env:TEMP\safeops-ca.crt"

Write-Host "==================================================" -ForegroundColor Cyan
Write-Host " SafeOps Root CA Installation Script" -ForegroundColor Cyan
Write-Host "==================================================" -ForegroundColor Cyan
Write-Host ""

# Check if running as Administrator
$currentPrincipal = New-Object Security.Principal.WindowsPrincipal([Security.Principal.WindowsIdentity]::GetCurrent())
if (-not $currentPrincipal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)) {
    Write-Host "ERROR: Please run as Administrator" -ForegroundColor Red
    Write-Host "Right-click the script -> Run as Administrator" -ForegroundColor Yellow
    pause
    exit 1
}

try {
    # Download CA certificate
    Write-Host "[1/3] Downloading CA certificate..." -ForegroundColor White
    [Net.ServicePointManager]::SecurityProtocol = [Net.SecurityProtocolType]::Tls12
    Invoke-WebRequest -Uri $caUrl -OutFile $tempFile -UseBasicParsing

    if (-not (Test-Path $tempFile)) {
        throw "Failed to download certificate"
    }

    # Import certificate to Trusted Root store
    Write-Host "[2/3] Installing CA certificate to Trusted Root store..." -ForegroundColor White
    
    if (Get-Command Import-Certificate -ErrorAction SilentlyContinue) {
        # Windows 8+ / PowerShell 4+
        $cert = Import-Certificate -FilePath $tempFile -CertStoreLocation Cert:\LocalMachine\Root
        Write-Host "   Imported using Import-Certificate cmdlet" -ForegroundColor Gray
    } else {
        # Windows 7 / older PowerShell (use certutil)
        $result = certutil -addstore -f "Root" $tempFile
        if ($LASTEXITCODE -ne 0) {
            throw "certutil failed with exit code $LASTEXITCODE"
        }
        Write-Host "   Imported using certutil" -ForegroundColor Gray
    }

    # Cleanup
    Write-Host "[3/3] Cleaning up..." -ForegroundColor White
    Remove-Item $tempFile -Force -ErrorAction SilentlyContinue

    # Success
    Write-Host ""
    Write-Host "==================================================" -ForegroundColor Green
    Write-Host " Installation Complete!" -ForegroundColor Green
    Write-Host "==================================================" -ForegroundColor Green
    Write-Host "✓ Certificate: $caName" -ForegroundColor Green
    Write-Host "✓ Store: Trusted Root Certification Authorities" -ForegroundColor Green
    Write-Host "✓ Status: Trusted" -ForegroundColor Green
    {{if .Fingerprint}}Write-Host "✓ Fingerprint: {{.Fingerprint}}" -ForegroundColor Green{{end}}
    Write-Host ""
    Write-Host "You may need to restart your browser for changes to take effect." -ForegroundColor Yellow

} catch {
    Write-Host ""
    Write-Host "ERROR: Installation failed" -ForegroundColor Red
    Write-Host $_.Exception.Message -ForegroundColor Red
    
    # Cleanup on error
    if (Test-Path $tempFile) {
        Remove-Item $tempFile -Force -ErrorAction SilentlyContinue
    }
    
    pause
    exit 1
}

Write-Host ""
Write-Host "Press any key to exit..."
$null = $Host.UI.RawUI.ReadKey("NoEcho,IncludeKeyDown")
`

