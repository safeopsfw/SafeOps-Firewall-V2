// NewScriptConfig creates a new ScriptConfig with default values.
func NewScriptConfig(baseURL string) *ScriptConfig {
	// Ensure baseURL doesn't end with slash
	baseURL = strings.TrimSuffix(baseURL, "/")

	return &ScriptConfig{
		BaseURL:         baseURL,
		CACertURL:       baseURL + "/ca.crt",
		CADerURL:        baseURL + "/ca.der",
		CertificateName: "SafeOps Root CA",
		Organization:    "SafeOps",
		RequiresSudo:    true,
	}
}

// WithFingerprint adds a fingerprint to the script configuration.
func (c *ScriptConfig) WithFingerprint(fingerprint string) *ScriptConfig {
	c.Fingerprint = fingerprint
	return c
}

// WithOrganization sets the organization name.
func (c *ScriptConfig) WithOrganization(org string) *ScriptConfig {
	c.Organization = org
	return c
}

// WithCertificateName sets the certificate display name.
func (c *ScriptConfig) WithCertificateName(name string) *ScriptConfig {
	c.CertificateName = name
	return c
}

