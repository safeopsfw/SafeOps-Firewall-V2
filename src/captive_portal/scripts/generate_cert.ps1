# ============================================================================
# SafeOps Captive Portal - Self-Signed Certificate Generator
# ============================================================================
# File: D:\SafeOpsFV2\src\captive_portal\scripts\generate_cert.ps1
# Purpose: Generate self-signed TLS certificate for Captive Portal HTTPS
# 
# Why Self-Signed?
#   - Captive Portal serves devices BEFORE they have the Root CA installed
#   - Cannot use Step-CA signed cert (chicken-and-egg problem)
#   - Users will see browser warning, must click "Proceed anyway"
#   - This is expected and acceptable for captive portal flows
#
# Certificate Details:
#   - Algorithm: RSA 2048-bit (compatible with all browsers)
#   - Validity: 1 year (365 days)
#   - Subject: CN=captive.safeops.local
#   - SANs: DNS and IP addresses for portal access
#
# Usage:
#   .\scripts\generate_cert.ps1
#   .\scripts\generate_cert.ps1 -Regenerate
#
# Prerequisites:
#   - OpenSSL 3.x installed and in PATH
#
# Author: SafeOps Phase 3A
# Date: 2026-01-03
# ============================================================================

[CmdletBinding()]
param(
    # Force regeneration even if certificates exist
    [Parameter()]
    [switch]$Regenerate,

    # Certificate validity in days
    [Parameter()]
    [int]$ValidityDays = 365,

    # RSA key size
    [Parameter()]
    [ValidateSet(2048, 4096)]
    [int]$KeySize = 2048,

    # Primary hostname
    [Parameter()]
    [string]$CommonName = "captive.safeops.local",

    # Output directory
    [Parameter()]
    [string]$OutputDir = "D:\SafeOpsFV2\src\captive_portal\internal\certs"
)

# ============================================================================
# Configuration
# ============================================================================

$ErrorActionPreference = 'Stop'
Set-StrictMode -Version Latest

# Certificate file paths
$keyFile = Join-Path $OutputDir "server.key"
$certFile = Join-Path $OutputDir "server.crt"
$csrFile = Join-Path $OutputDir "server.csr"
$configFile = Join-Path $OutputDir "openssl.cnf"

# Subject Alternative Names (SANs)
# These are all the ways users might access the captive portal
$sanDNS = @(
    "captive.safeops.local",
    "portal.safeops.local",
    "safeops.local",
    "localhost"
)

$sanIP = @(
    "192.168.137.1",   # Mobile Hotspot gateway IP
    "127.0.0.1"        # Localhost
)

# ============================================================================
# Functions
# ============================================================================

