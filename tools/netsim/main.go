// ============================================================================
// Test: Blocked Domain DNS
// Resolves domains from domains.txt — should be redirected to 127.0.0.1.
// ============================================================================
func testBlockedDomains() testResult {
	sent := 0
	for _, domain := range blockedDomains {
		ips, err := net.LookupHost(domain)
		if err != nil {
			fmt.Printf("    %s — "+green+"blocked (DNS failed)"+reset+"\n", domain)
		} else if len(ips) > 0 && ips[0] == "127.0.0.1" {
			fmt.Printf("    %s → 127.0.0.1 — "+green+"redirected to block page"+reset+"\n", domain)
		} else {
			fmt.Printf("    %s → %s — "+yellow+"resolved (not blocked?)"+reset+"\n", domain, strings.Join(ips, ", "))
		}
		sent++
	}

	return testResult{true, sent, fmt.Sprintf("Blocked domains: %d DNS queries", sent)}
}

// ============================================================================
// Test: Rapid Connections (Rate Limiting)
// Opens 500 TCP connections in rapid burst from one source to one target.
// This tests the rate limiter (1000 pkt/s per IP, burst 2000).
// ============================================================================
func testRapidConnections(target string) testResult {
	sent := 0
	var wg sync.WaitGroup

	// Send 500 connections concurrently in bursts of 50
	for batch := 0; batch < 10; batch++ {
		for i := 0; i < 50; i++ {
			wg.Add(1)
			port := rand.Intn(65534) + 1
			go func(p int) {
				defer wg.Done()
				conn, err := net.DialTimeout("tcp", fmt.Sprintf("%s:%d", target, p), 50*time.Millisecond)
				if err == nil {
					conn.Close()
				}
			}(port)
			sent++
		}
		wg.Wait()
		time.Sleep(10 * time.Millisecond) // Brief pause between bursts
	}

	return testResult{true, sent, fmt.Sprintf("Rapid connections: %d TCP connections in burst", sent)}
}

// ============================================================================
// Test: Multi-Protocol Burst
// Sends TCP + UDP simultaneously from the same source IP to stress test.
// ============================================================================
func testMultiProtoBurst(target string, duration time.Duration) testResult {
	var sent int64
	start := time.Now()
	var wg sync.WaitGroup

	// TCP goroutine
	wg.Add(1)
	go func() {
		defer wg.Done()
		for time.Since(start) < duration {
			for i := 0; i < 100; i++ {
				port := rand.Intn(65534) + 1
				conn, err := net.DialTimeout("tcp", fmt.Sprintf("%s:%d", target, port), 10*time.Millisecond)
				if err == nil {
					conn.Close()
				}
				atomic.AddInt64(&sent, 1)
			}
			time.Sleep(100 * time.Millisecond)
		}
	}()

	// UDP goroutine
	wg.Add(1)
	go func() {
		defer wg.Done()
		payload := make([]byte, 256)
		rand.Read(payload)
		addr, _ := net.ResolveUDPAddr("udp", fmt.Sprintf("%s:9999", target))
		conn, err := net.DialUDP("udp", nil, addr)
		if err != nil {
			return
		}
		defer conn.Close()
		for time.Since(start) < duration {
			for i := 0; i < 200; i++ {
				conn.Write(payload)
				atomic.AddInt64(&sent, 1)
			}
			time.Sleep(100 * time.Millisecond)
		}
	}()

	wg.Wait()

	return testResult{true, int(sent), fmt.Sprintf("Multi-protocol burst: %d TCP+UDP packets in %v", sent, duration)}
}

