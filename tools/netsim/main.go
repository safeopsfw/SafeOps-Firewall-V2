// netsim — Real network traffic simulator for SafeOps Firewall testing
//
// Unlike sectest (which calls security modules directly with fake data),
// netsim generates REAL network traffic that flows through the full pipeline:
//
//   netsim → real TCP/UDP packets → SafeOps Engine (tunnel) → gRPC →
//   Firewall Engine → security checks → REAL alerts in firewall-alerts.jsonl
//
// The simulator sends packets to a non-routable external IP (198.51.100.1,
// TEST-NET-2 per RFC 5737) so they hit the SafeOps Engine's tunnel but
// never actually leave the network. The packets are still parsed, checked
// by the Firewall Engine security modules, and generate real alerts.
//
// Usage:
//
//	go run .                 (run all tests)
//	go run . --test syn      (run specific test)
//	go run . --list          (list available tests)
//
// Must run WHILE safeops-engine + firewall-engine are both running.
// Run as Administrator for best results.
