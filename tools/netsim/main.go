type testCase struct {
	name        string
	description string
	fn          func() testResult
}

type testResult struct {
	passed      bool
	packetsOut  int
	description string
}

func main() {
	testFlag := flag.String("test", "", "Run specific test (comma-separated names)")
	listFlag := flag.Bool("list", false, "List all available tests")
	durationFlag := flag.Int("duration", 3, "Duration in seconds for flood tests")
	flag.Parse()

	fmt.Println(bold + cyan + `
╔══════════════════════════════════════════════════════════════════╗
║   SafeOps Network Attack Simulator v2.0 (Real Traffic Mode)    ║
║   Generates REAL packets → SafeOps Engine → Firewall Engine    ║
║   Alerts are generated by the FIREWALL ENGINE, not this tool   ║
╚══════════════════════════════════════════════════════════════════╝` + reset)
	fmt.Println()

	duration := time.Duration(*durationFlag) * time.Second

	// Test order matters: DDoS floods and rule-engine tests run FIRST because
	// port scan and brute force tests trigger IP bans, which would cause all
	// subsequent tests from the same source IP to be dropped at the ban check.
	tests := []testCase{
		{"vpn-ports", "VPN Port Access — connect to blocked VPN/Tor ports", func() testResult { return testVPNPorts() }},
		{"doh-bypass", "DoH Bypass — connect to known DoH resolver IPs on 443", func() testResult { return testDoHBypass() }},
		{"dns-block", "Blocked Domain DNS — resolve blocked domains", func() testResult { return testBlockedDomains() }},
		{"udp-flood", "UDP Flood DDoS — 5500+ UDP/sec (threshold: 5000)", func() testResult { return testUDPFlood(targetIP2, duration) }},
		{"syn-flood", "SYN Flood DDoS — 1100+ SYN/sec (threshold: 1000)", func() testResult { return testSYNFlood(targetIP, duration) }},
		{"ssh-brute", "SSH Brute Force — 7 attempts on port 22 (threshold: 5/120s)", func() testResult { return testBruteForce(targetIP5, 22, "SSH", 7) }},
		{"rdp-brute", "RDP Brute Force — 5 attempts on port 3389 (threshold: 3/60s)", func() testResult { return testBruteForce(targetIP6, 3389, "RDP", 5) }},
		{"rapid-conn", "Rapid Connections — 500 connections in burst (rate limiting)", func() testResult { return testRapidConnections(targetIP7) }},
		{"port-scan", "Port Scan — 150 unique ports in <10s (threshold: 100)", func() testResult { return testPortScan(targetIP3) }},
		{"seq-scan", "Sequential Port Scan — 30 sequential (threshold: 20)", func() testResult { return testSequentialScan(targetIP4) }},
		{"multi-proto", "Multi-Protocol Burst — TCP+UDP from same IP simultaneously", func() testResult { return testMultiProtoBurst(targetIP8, duration) }},
	}

	if *listFlag {
		fmt.Println(bold + "Available tests:" + reset)
		for _, t := range tests {
			fmt.Printf("  %-14s %s\n", t.name, t.description)
		}
		return
	}

	// Filter tests if --test flag given
	if *testFlag != "" {
		names := strings.Split(*testFlag, ",")
		nameSet := make(map[string]bool)
		for _, n := range names {
			nameSet[strings.TrimSpace(n)] = true
		}

		var filtered []testCase
		for _, t := range tests {
			if nameSet[t.name] {
				filtered = append(filtered, t)
			}
		}
		if len(filtered) == 0 {
			fmt.Printf(red+"No tests match: %s\n"+reset, *testFlag)
			fmt.Println("Use --list to see available tests")
			os.Exit(1)
		}
		tests = filtered
	}

	fmt.Printf(yellow+"Target IPs: %s (TEST-NET-2, non-routable)\n"+reset, targetIP)
	fmt.Printf(yellow + "Make sure safeops-engine + firewall-engine are BOTH running!\n" + reset)
	fmt.Printf(yellow+"Flood duration: %v\n"+reset, duration)
	fmt.Println(strings.Repeat("─", 64))

	passed := 0
	failed := 0

	for i, t := range tests {
		fmt.Printf("\n"+bold+cyan+"[%d/%d] %s"+reset+"\n", i+1, len(tests), t.description)

		result := t.fn()

		if result.passed {
			fmt.Printf(green+"  ✓ DONE: %s (%d packets sent)\n"+reset, result.description, result.packetsOut)
			passed++
		} else {
			fmt.Printf(red+"  ✗ FAIL: %s\n"+reset, result.description)
			failed++
		}

		// Brief pause between tests to let the firewall engine process
		if i < len(tests)-1 {
			time.Sleep(500 * time.Millisecond)
		}
	}

	fmt.Println("\n" + strings.Repeat("═", 64))
	fmt.Printf(bold+"Results: %d/%d tests completed\n"+reset, passed, passed+failed)
	if failed > 0 {
		fmt.Printf(red+"  %d tests had errors sending packets\n"+reset, failed)
	}
	fmt.Println()
	fmt.Println(yellow + "Now check your Firewall Engine output for alerts!" + reset)
	fmt.Println(yellow + "Alert file: bin/firewall-engine/data/alerts/firewall-alerts.jsonl" + reset)
}

// ============================================================================
// Test: SYN Flood DDoS
// Sends many TCP SYN connections to random ports from the same source.
// The SafeOps Engine sees these as outbound TCP to port X on targetIP,
// sends them via gRPC to Firewall Engine, which detects SYN flood.
// ============================================================================
func testSYNFlood(target string, duration time.Duration) testResult {
	var sent int64
	start := time.Now()
	var wg sync.WaitGroup

	// Use a SINGLE port to avoid triggering port scan detection.
	// Real SYN floods target a specific service port (e.g., 80, 443).
	floodPort := 80

	for time.Since(start) < duration {
		// Launch 200 concurrent TCP dials per batch — each generates a SYN packet.
		// Using goroutines because DialTimeout blocks waiting for SYN-ACK from
		// a non-routable IP, so sequential dialing is too slow.
		for i := 0; i < 200; i++ {
			wg.Add(1)
			go func() {
				defer wg.Done()
				conn, err := net.DialTimeout("tcp", fmt.Sprintf("%s:%d", target, floodPort), 10*time.Millisecond)
				if err == nil {
					conn.Close()
				}
				atomic.AddInt64(&sent, 1)
			}()
		}
		time.Sleep(50 * time.Millisecond)
	}
	wg.Wait()

	return testResult{true, int(sent), fmt.Sprintf("SYN flood: %d SYN packets to port %d in %v", sent, floodPort, duration)}
}

// ============================================================================
// Test: UDP Flood DDoS
// Sends many UDP packets to random ports.
// ============================================================================
func testUDPFlood(target string, duration time.Duration) testResult {
	sent := 0
	payload := make([]byte, 512)
	rand.Read(payload)
	start := time.Now()

	addr, err := net.ResolveUDPAddr("udp", fmt.Sprintf("%s:%d", target, 9999))
	if err != nil {
		return testResult{false, 0, fmt.Sprintf("resolve error: %v", err)}
	}

	conn, err := net.DialUDP("udp", nil, addr)
	if err != nil {
		return testResult{false, 0, fmt.Sprintf("dial error: %v", err)}
	}
	defer conn.Close()

	for time.Since(start) < duration {
		for i := 0; i < 550; i++ { // ~5500/sec in batches
			conn.Write(payload)
			sent++
		}
		time.Sleep(100 * time.Millisecond)
	}

	return testResult{true, sent, fmt.Sprintf("UDP flood: %d packets in %v", sent, duration)}
}

// ============================================================================
// Test: Port Scan
// Rapidly connects to 150 unique ports on the target.
// ============================================================================
func testPortScan(target string) testResult {
	ports := make([]int, 150)
	for i := range ports {
		ports[i] = rand.Intn(65534) + 1
	}

	sent := 0
	for _, port := range ports {
		conn, err := net.DialTimeout("tcp", fmt.Sprintf("%s:%d", target, port), 20*time.Millisecond)
		if err == nil {
			conn.Close()
		}
		sent++
	}

	return testResult{true, sent, fmt.Sprintf("Port scan: %d unique ports probed", sent)}
}

